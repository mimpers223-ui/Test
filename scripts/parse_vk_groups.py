# Паттерны для поиска топлива
FUEL_KEYWORDS = {
    "92":     ["92", "аи-92", "аи92", "а92"],
    "95":     ["95", "аи-95", "аи95", "а95"],
    "98":     ["98", "аи-98", "аи98"],
    "100":    ["100", "аи-100", "аи100"],
    "diesel": ["дизель", "диз", "солярка", "дт", "дп"],
    "lpg":    ["газ", "пропан", "lpg"],
}

YES_WORDS = ["есть", "завезли", "привезли", "появилось", "в наличии", "льют", "работает", "налили", "горит", "засветился"]
NO_WORDS = ["нет", "отсутствует", "пусто", "закончился", "кончился", "нету", "закончили"]
NO_EXCLUDE = ["нет очереди", "очереди нет", "без очереди"]
LOW_WORDS = ["мало", "заканчивается", "кончается", "осталось мало", "на исходе"]

PRICE_RE = re.compile(
    r"(?:аи-?)?(\d{2,3}|дизель|диз|дт|газ)\s*(?:по|[-\-:=])\s*(\d{2,3}[.,]?\d{0,2})\s*(?:руб|₽)?",
    re.IGNORECASE
)

# Надёжность VK групп (по типу контента)
VK_GROUP_RELIABILITY = {
    "depletion": 0.90,  # Группы про дефицит — точные данные об отсутствии
    "availability": 0.85,  # Группы про наличие
    "prices": 0.70,  # Группы про цены
    "general": 0.60,  # Общие группы
}


def parse_vk_wall_text(text: str, city: str) -> list[dict]:
    """Парсит текст сообщения со стены VK-группы."""
    if not text or len(text) < 10:
        return []

    text_lower = text.lower()
    results = []

    # Ищем упоминания топлива
    for fuel, keywords in FUEL_KEYWORDS.items():
        for kw in keywords:
            if kw in text_lower:
                # Контекст ±80 символов
                idx = text_lower.find(kw)
                ctx_start = max(0, idx - 80)
                ctx_end = min(len(text), idx + len(kw) + 80)
                ctx = text_lower[ctx_start:ctx_end]

                available = None
                has_no = any(w in ctx for w in NO_WORDS)
                has_no_excl = any(e in ctx for e in NO_EXCLUDE)
                has_yes = any(w in ctx for w in YES_WORDS)
                has_low = any(w in ctx for w in LOW_WORDS)

                if has_yes:
                    available = True
                elif has_no and not has_no_excl:
                    available = False
                elif has_low:
                    available = None

                # Цена
                price = None
                for m in PRICE_RE.finditer(text):
                    matched_fuel = m.group(1).lower()
                    if matched_fuel in ("диз", "дт", "газ"):
                        matched_fuel = "diesel" if matched_fuel != "газ" else "lpg"
                    if matched_fuel == fuel:
                        try:
                            p = float(m.group(2).replace(",", "."))
                            if 20 < p < 200:
                                price = p
                        except ValueError:
                            pass

                results.append({
                    "fuel_type": fuel,
                    "available": available,
                    "price": price,
                    "city": city,
                })
                break  # Один результат на вид топлива

    return results


async def scrape_vk_group(group_id: str, city: str, limit: int = 100) -> list[dict]:
    """Скрейпинг стены VK-группы через публичный API."""
    import aiohttp

    results = []
    url = f"https://api.vk.com/method/wall.get"
    params = {
        "domain": group_id,
        "count": min(limit, 100),
        "v": "5.199",
        "access_token": "",  # Публичный доступ без токена
    }

    try:
        async with aiohttp.ClientSession() as session:
            async with session.get(
                url,
                params=params,
                timeout=aiohttp.ClientTimeout(total=30),
            ) as resp:
                if resp.status != 200:
                    logger.debug("VK API %s: HTTP %d", group_id, resp.status)
                    return results

                data = await resp.json()
                if "error" in data:
                    # Публичный доступ может быть ограничен
                    logger.debug("VK API %s: %s", group_id, data["error"].get("error_msg", ""))
                    return results

                items = data.get("response", {}).get("items", [])
                for item in items:
                    text = item.get("text", "")
                    post_time = datetime.fromtimestamp(item.get("date", 0))

                    parsed = parse_vk_wall_text(text, city)
                    for p in parsed:
                        p["post_time"] = post_time
                        p["source"] = "vk"
                        p["group_id"] = group_id
                    results.extend(parsed)

                logger.info("VK %s (%s): found %d reports", group_id, city, len(results))

    except Exception as e:
        logger.debug("VK %s error: %s", group_id, e)

    return results


async def run_vk_parser(cities: list[str] = None, limit_per_group: int = 50) -> int:
    """Запуск парсера VK-групп."""
    if not cities:
        cities = list(set(VK_FUEL_GROUPS.values()))

    total_saved = 0

    for city in cities:
        groups = [gid for gid, c in VK_FUEL_GROUPS.items() if c == city]
        if not groups:
            continue

        logger.info("VK city: %s (%d groups)", city, len(groups))

        for group_id in groups:
            results = await scrape_vk_group(group_id, city, limit_per_group)

            for r in results:
                # Ищем станцию в БД
                station_id = None
                stations = await db.find_stations_by_city(city=city, limit=1)
                if stations:
                    station_id = stations[0].get("id")

                if not station_id:
                    continue

                # Сохраняем отчёт
                await db.add_report(
                    station_id=station_id,
                    fuel_type=r["fuel_type"],
                    available=r.get("available"),
                    price=r.get("price"),
                    source="vk",
                    comment=f"vk:{group_id}: {r.get('text', '')[:200]}",
                )
                total_saved += 1

            await asyncio.sleep(0.5)  # Не ддосить VK

    logger.info("VK parser total: %d reports saved", total_saved)
    return total_saved


